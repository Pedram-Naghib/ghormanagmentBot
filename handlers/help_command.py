"""
handlers/help_command.py
---------------------------
/help و راهنما — Persian, plain language.

*** IMPORTANT: update HELP_TEXT whenever a feature changes, so admins
always see accurate instructions. ***
"""

from telebot.types import Message

from core import bot

HELP_TEXT = """
🤖 <b>راهنمای ربات مدیریت گروه</b>

━━━━━━━━━━━━━━
👮‍♂️ <b>سطوح دسترسی</b>

• <b>مالک (Owner)</b>: در تنظیمات سرور ربات مشخص می‌شود؛ می‌تواند ادمین ربات اضافه/حذف کند.
• <b>ادمین ربات (Bot Admin)</b>: توسط مالک تعیین می‌شود و در <u>همهٔ گروه‌هایی</u> که این ربات
  عضو آن‌هاست دسترسی مدیریتی دارد؛ حتی اگر در تلگرام ادمین آن گروه خاص نباشد.
• <b>ادمین گروه</b>: ادمین‌های واقعی تلگرام همان گروه، این‌ها هم به‌طور خودکار دسترسی مدیریتی دارند.
• <b>ویژه (VIP)</b>: از محدودیت‌های ضد اسپم معاف است.
• <b>عادی</b>: مشمول همهٔ محدودیت‌های زیر.

━━━━━━━━━━━━━━
👮‍♂️ <b>دستورات مدیریتی</b>
(روی پیام کاربر مورد نظر ریپلای کنید و یکی از این کلمات را بفرستید)

• <b>کیک</b> / <b>بن</b> / <b>اخراج</b> → کاربر را از گروه اخراج <u>و بن</u> می‌کند؛
  یعنی هم حذف می‌شود، هم تا وقتی «رفع بن» نشود نمی‌تواند با لینک دعوت برگردد.
• <b>رفع بن</b> یا <b>آنبن</b> → کاربر را از بن خارج می‌کند.
  اگر پیامی از او برای ریپلای‌کردن ندارید، می‌توانید بنویسید: <code>رفع بن @username</code>
  (این روش فقط زمانی کار می‌کند که آن کاربر قبلاً در همین گروه پیام داده باشد.)
• <b>میوت</b> یا <b>سکوت</b> → کاربر را به مدت ۲۴ ساعت سکوت (بی‌صدا) می‌کند.
• <b>تنظیم ویژه</b> → کاربر را عضو ویژه (VIP) می‌کند تا از محدودیت‌های زیر معاف شود.

━━━━━━━━━━━━━━
🛠 <b>مدیریت ادمین‌های ربات</b> (فقط مالک ربات)

• <b>افزودن ادمین</b> (ریپلای روی پیام کاربر) → او را ادمین ربات می‌کند (در همهٔ گروه‌ها).
• <b>حذف ادمین</b> (ریپلای) → دسترسی ادمین ربات را از او می‌گیرد.
• <b>لیست ادمین ها</b> → فهرست ادمین‌های ربات را نشان می‌دهد.

━━━━━━━━━━━━━━
🛡 <b>تنظیم آستانهٔ اسپم</b> (مستقیم از داخل گروه، بدون نیاز به تغییر تنظیمات سرور)

• <code>تنظیم اسپم [حداکثر پیام] [بازه به ثانیه] [مدت سکوت به دقیقه]</code>
  مثال: <code>تنظیم اسپم 6 8 30</code> یعنی «بیش از ۶ پیام در ۸ ثانیه = ۳۰ دقیقه سکوت»
• <b>تنظیمات اسپم</b> → نمایش تنظیمات فعلی همین گروه.

هر گروه تنظیمات مستقل خودش را دارد؛ تغییر در یک گروه روی گروه‌های دیگر اثر نمی‌گذارد.

━━━━━━━━━━━━━━
📊 <b>آمار گروه</b>

• <b>آمار روزانه</b> → گزارش فعالیت ۲۴ ساعت گذشته (پیام هر عضو + عضوهای اضافه‌شده).
• <b>آمار کل</b> → همان گزارش از ابتدای فعالیت گروه تاکنون.

━━━━━━━━━━━━━━
👤 <b>پروفایل کاربر</b>

روی پیام یک کاربر ریپلای کنید و بنویسید <b>پروفایل</b> (یا /profile) تا نام، عکس پروفایل،
سطح دسترسی، تعداد کل پیام‌ها و پیام‌های ۲۴ ساعت اخیر او را ببینید.

━━━━━━━━━━━━━━
🛡 <b>قوانین خودکار برای اعضای عادی</b>

• ارسال لینک یا فوروارد کردن پیام از کانال‌ها → پیام بلافاصله حذف می‌شود.
• ارسال پیام‌های زیاد در زمان کوتاه (اسپم) → سکوت خودکار
  (آستانه با دستور «تنظیم اسپم» در همین گروه قابل تغییر است).

اعضای ویژه، ادمین‌های ربات، و ادمین‌های گروه از این قوانین معاف هستند.

━━━━━━━━━━━━━━
ℹ️ ربات باید در گروه <b>ادمین</b> باشد (با دسترسی Ban users، Delete messages، Restrict members).
این ربات می‌تواند همزمان عضو چند گروه باشد؛ آمار و تنظیمات هر گروه کاملاً جداگانه ذخیره می‌شود.

این راهنما با هر آپدیت ربات به‌روزرسانی می‌شود تا همیشه دقیق باشد.
"""


@bot.message_handler(commands=["help"])
async def help_command(message: Message):
    await bot.reply_to(message, HELP_TEXT)


@bot.message_handler(func=lambda m: m.text and m.text.strip() == "راهنما")
async def help_command_fa(message: Message):
    await bot.reply_to(message, HELP_TEXT)