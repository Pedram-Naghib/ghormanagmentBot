"""
handlers/help_command.py
---------------------------
/help و راهنما — Persian, plain language.

*** IMPORTANT: update HELP_TEXT whenever a feature changes, so it always
matches what the bot actually does. ***
"""

from telebot.types import Message

from core import bot

HELP_TEXT = """
🤖 <b>راهنمای ربات مدیریت گروه</b>

━━━━━━━━━━━━━━
👑 <b>سطوح دسترسی</b> (هر گروه، مستقل از گروه‌های دیگر)

• <b>مالک ربات (Global Owner)</b>: در تنظیمات سرور ربات مشخص می‌شود؛ در همهٔ
  گروه‌ها همیشه دسترسی کامل دارد.
• <b>مالک گروه</b>: کسی که ربات را به یک گروه <u>اضافه کرده</u>. به‌طور خودکار
  ثبت می‌شود و فقط در همان گروه دسترسی کامل دارد. او می‌تواند برای گروهش
  ادمین و عضو ویژه تعیین کند.
• <b>ادمین گروه</b>: کسی که مالک همان گروه او را تعیین کرده. فقط در همان
  گروه دسترسی مدیریتی کامل دارد (نمی‌تواند ادمین دیگری اضافه کند).
• <b>عضو ویژه (VIP)</b>: فقط در همان گروه از محدودیت‌های ضد اسپم معاف است.
  عضو ویژهٔ یک گروه، در گروه دیگر یک عضو <u>عادی</u> است مگر آنجا هم به او
  این دسترسی داده شود.
• <b>عضو عادی</b>: مشمول همهٔ محدودیت‌های زیر.

ℹ️ توجه: ادمین بودن در خودِ تلگرام به‌تنهایی دسترسی به دستورات ربات نمی‌دهد؛
فقط نقش‌های بالا این دسترسی را می‌دهند.

━━━━━━━━━━━━━━
👮‍♂️ <b>دستورات مدیریتی</b>
(روی پیام کاربر مورد نظر ریپلای کنید و یکی از این کلمات را بفرستید)

• <b>کیک</b> / <b>بن</b> / <b>اخراج</b> → کاربر را از گروه اخراج <u>و بن</u> می‌کند؛
  تا وقتی «رفع بن» نشود نمی‌تواند با لینک دعوت برگردد.
• <b>رفع بن</b> یا <b>آنبن</b> → کاربر را از بن خارج می‌کند.
  اگر پیامی از او ندارید: <code>رفع بن @username</code>
• <b>میوت</b> یا <b>سکوت</b> → کاربر را ۲۴ ساعت سکوت می‌کند.
• <b>تنظیم ویژه</b> → کاربر را عضو ویژهٔ همین گروه می‌کند.
• <b>لغو ویژه</b> → عضویت ویژهٔ او را در همین گروه لغو می‌کند.

━━━━━━━━━━━━━━
👑 <b>مالکیت و ادمین‌های گروه</b>

• <b>مالک این گروه</b> → نشان می‌دهد چه کسی مالک این گروه است.
• <b>ادعای مالکیت</b> → اگر گروه هنوز مالک ثبت‌شده ندارد (مثلاً ربات قبل از
  این قابلیت اضافه شده)، یک ادمین واقعی تلگرام می‌تواند مالکیت را ادعا کند.
• <b>افزودن ادمین گروه</b> (ریپلای، فقط مالک گروه) → کاربر را ادمین همین گروه می‌کند.
• <b>حذف ادمین گروه</b> (ریپلای، فقط مالک گروه) → دسترسی ادمین را می‌گیرد.
• <b>لیست ادمین های گروه</b> → مالک و ادمین‌های همین گروه را نشان می‌دهد.

━━━━━━━━━━━━━━
🛡 <b>تنظیم آستانهٔ اسپم</b> (مستقیم از داخل گروه)

• <code>تنظیم اسپم [حداکثر پیام] [بازه به ثانیه] [مدت سکوت به دقیقه]</code>
  مثال: <code>تنظیم اسپم 6 8 30</code>
• <b>تنظیمات اسپم</b> → نمایش تنظیمات فعلی همین گروه.

━━━━━━━━━━━━━━
📊 <b>آمار گروه</b>

• <b>آمار روزانه</b> → فعالیت ۲۴ ساعت گذشته.
• <b>آمار کل</b> → فعالیت کل از ابتدا.

━━━━━━━━━━━━━━
👤 <b>پروفایل کاربر</b>

روی پیام کسی ریپلای کنید و بنویسید <b>پروفایل</b> (یا /profile) تا نام، عکس
پروفایل، نقش او در همین گروه، و آمار پیام‌هایش را ببینید.

━━━━━━━━━━━━━━
🛡 <b>قوانین خودکار برای اعضای عادی</b>

• ارسال لینک یا فوروارد از کانال‌ها → پیام فوراً حذف می‌شود.
• ارسال پیام‌های زیاد در زمان کوتاه → سکوت خودکار (آستانه با «تنظیم اسپم» قابل تغییر است).

━━━━━━━━━━━━━━
ℹ️ ربات باید در گروه <b>ادمین</b> باشد (با دسترسی Ban users، Delete messages، Restrict members).
این ربات می‌تواند همزمان عضو چند گروه باشد؛ نقش‌ها، آمار و تنظیمات هر گروه کاملاً جداگانه‌اند.

این راهنما با هر آپدیت ربات به‌روزرسانی می‌شود.
"""


@bot.message_handler(commands=["help"])
async def help_command(message: Message):
    await bot.reply_to(message, HELP_TEXT)


@bot.message_handler(func=lambda m: m.text and m.text.strip() == "راهنما")
async def help_command_fa(message: Message):
    await bot.reply_to(message, HELP_TEXT)